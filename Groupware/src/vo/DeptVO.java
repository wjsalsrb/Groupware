package vo;

public class DeptVO {
	private int deptNo = 0;
	private String deptName = "";

	public int getDeptNo() {
		return deptNo;
	}
	public DeptVO setDeptNo(int deptNo) {
		this.deptNo = deptNo;
		return this;
	}
	public String getDeptName() {
		return deptName;
	}
	public DeptVO setDeptName(String deptName) {
		this.deptName = deptName;
		return this;
	}
	
	
}
